//Library
#include <iostream>
using namespace std;
int main(){
    // enter temperature in celsius and covert into fahrenheit
	int cels;
	int far;
	float pro;
    //entering values
	cels=0;
	pro= cels * 1.8;
	far= pro + 32;
    //displaying values
	cout<<"Temperature in celsius = "<<cels<<endl;
	cout<<"Temperature in fahrenheit = "<<far<<endl;
return 0;
}
